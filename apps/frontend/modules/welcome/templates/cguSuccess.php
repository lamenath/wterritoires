	<h1 class="name">Conditions Générales d’Utilisation (Charte du Réseau)</h1>

	<p>
		<strong>Introduction</strong>
	</p>

	<p>
	Le Réseau rural de Picardie est un outil de réseau social (ci-après dénommé "réseau social RRR") orienté vers les projets et les problématiques du milieu rural. Le réseau social RRR est une initiative du comité de pilotage du Réseau rural de Picardie, réalisé avec le concours financier de l’Etat de la Région Picardie et du FEADER.
	</p>
	<p>
	Le réseau social RRR a pour objectif la mise en relation des acteurs du monde rural dans leur diversité pour favoriser une réflexion et un partage des travaux sur les problématiques du développement durable. Il vise à donner de la visibilité et de la lisibilité aux acteurs et à leurs projets sur le territoire picard.
	</p>
	<p>
	Le réseau RRR s’adresse particulièrement aux acteurs et habitants des territoires ruraux de la région Picardie mais reste ouvert à tous les observateurs, acteurs et contributeurs au niveau national ou européen.
	</p>
	<p>
	En tant que réseau social, RRR regroupe :<br />
	- des personnes physiques désignées par le terme "membre", l'inscription "membre" se fait par l'Utilisateur, nécessitant l’acceptation des présentes conditions générales d’utilisation (charte du réseau).<br />

	- des personnes morales désignées par le terme "structures", celles-ci sont référencées par les "membres".<br />
	- des "projets", créés par les "membres" et éventuellement associé à une "structure".
	</p>
	<p>
	<strong>1. CGU et Accès au service</strong>
	</p>
	<p>
	L’accès et l’utilisation du réseau RRR est gratuit. Pour accéder au site et Utiliser les services du réseau social RRR, l’Utilisateur doit créer un compte en suivant les étapes du formulaire d’inscription. Pour pouvoir s’inscrire et bénéficier des services du réseau social RRR, l’Utilisateur doit lire et accepter les présentes Conditions Générales d’Utilisation. Si le visiteur n'accepte pas les conditions d'utilisation du réseau social RRR, il ne doit pas l'utiliser. L’Utilisateur est informé que la création d’un compte sur le réseau RRR constitue une collecte de données personnelles et accepte cette collecte. Si l’Utilisateur s’oppose à la collecte de données personnelles, il ne doit pas créer de compte et utiliser la plateforme sans s’enregistrer (les contenus restent accessible aux personnes non inscrites via le moteur de recherche).
	</p>
	<p>
	<strong>Modification des présentes CGU</strong>
	</p>
	<p>
	Les présentes conditions d’utilisation peuvent être modifiées à tout moment, l'Utilisateur doit donc lire ces conditions à chaque visite sur le site du Réseau Social RRR. Ces modifications ne sont pas nécessairement notifiées aux Utilisateurs du réseau social RRR. Si l'Utilisateur continue d'utiliser le site de Réseau Social RRR après modification des conditions d'utilisation, cela signifie qu'il est en en accord avec les modifications apportées sur ces conditions.

	</p>
	<p>
	<strong>2. Services</strong>
	</p>
	<p>
	Deux accès au réseau RRR sont possibles :<br />
	- Accès sans inscription préalable (non membre) : cet accès de consultation, est accessible à tous les internautes sans nécessité d’inscription (création de compte membre) sur le réseau social RRR. Il permet de consulter, à l’aide du moteur de recherche, les contenus (fiches "membre", "structure" et "projet") publiés sur le réseau RRR. Certaines fiches membres ne sont pas accessibles aux internautes non membres. Enfin, les fonctionnalités de communication et de publication de contenus sont désactivées.<br />
	- Membre/Utilisateur (création préalable d’un compte "membre") : cet accès donne droit à toutes les fonctionnalités et accès à tous les contenus publiés sur le réseau.
	</p>
	<p>
	<strong>2.1. " membre "</strong>
	</p>
	<p>

	Introduction : Pour tirer le meilleur parti des outils et fonctionnalités proposés par le réseau social RRR, l’internaute est invité à créer son compte membre en respectant les étapes d’inscription décrites dans cette partie.
	</p>
	<p>
	<strong>2.1.1. inscription ou création d’un compte "membre"</strong>
	</p>
	<p>
	Lors de la phase d’inscription, l’Utilisateur fournit les informations qui apparaîtront sur son profil. Une fois le formulaire d’inscription complété, l’Utilisateur dispose d’une fiche "membre" (ou "profil"). Sa fiche est consultable ou non par les autres membres du réseau social RRR (cf. paragraphe sur la confidentialité 2.1.2.). IMPORTANT : l'inscription doit être personnelle et permettre aux autres Utilisateurs de la plateforme d'identifier la personne inscrite. <strong>L'inscription ne doit pas être complétée avec le nom d'une Entreprise ou d'une Organisation que l'Utilisateur veut représenter ou promouvoir</strong>. Une fois inscrit selon ce principe, l'Utilisateur pourra référencer et compléter une fiche dédiée à son Entreprise ou à son Organisation. Si l'Utilisateur ne souhaite pas que son profil soit visible des autres utilisateurs, il peut sélectionner le paramètre de confidentialité "privé" lors de son inscription (cf. partie 2.1.2.).
	</p>
	<p>
	<strong>Structuration des informations :</strong> lors de son inscription, l’Utilisateur est invité à respecter des champs formatés pour bien définir son profil. Ce fonctionnement permet de répondre à l’objectif de lisibilité des informations des acteurs du Réseau Rural et permet au système de suggérer des contacts et des contenus en lien avec les préoccupations de chaque Utilisateur. Les 4 informations principales demandées lors de l’inscription sont Filières, Thèmes, Métiers, Compétences. Ces familles de mot-clé régissent le fonctionnement de la plateforme et permettent de la structurer. Les administrateurs de la plateforme de réseau social RRR se réservent donc le droit d'ajouter, de modifier, de corriger, de reclasser les mots-clés saisis par l'Utilisateur lors de son inscription, dans un but de lisibilité et de précision du service.
	</p>
	<p>

	<strong>Filières</strong> : l’Utilisateur est invité à taper le nom des filières dans lesquelles il est impliqué, que ce soit à titre professionnel, personnel ou associatif. On propose à l’Utilisateur de choisir jusqu’à 3 filières. L’Utilisateur pourra à tout moment modifier ses 3 filières de prédilection en se rendant dans "mon profil". Exemples de filières : Avicole, Agricole, Bois, Energie, Social, Education, etc.
	</p>
	<p>
	<strong>Thèmes</strong> : les Thèmes sont des mots-clés ou expressions-clés spécifiques à une filière ou transversaux. Un grand choix de thèmes est déjà référencé dans l’outil Internet RRR. Les thèmes référencés s’afficheront en tant que suggestion pendant que l’Utilisateur commence à taper une expression. Afin d’assurer l’efficacité de l’outil, l’Utilisateur est invité à sélectionner la suggestion la plus adaptée dans le menu déroulant. Il doit éviter de reformuler une expression déjà suggérée. Exemples de thèmes : développement économique, emploi, reprise d’entreprise, traditions rurales, veille territoriale, urbanisme, handicap, éducation agricole, etc.
	</p>
	<p>
	<strong>Compétences</strong> : l’Utilisateur est invité à saisir jusqu’à 20 compétences qu’il souhaite mettre en avant sur son profil et partager ou valoriser en tant qu'acteur du réseau rural régional. Ces compétences peuvent être très spécifiques ou transversales. Exemple de compétences spécifiques : Exemple de compétences transversales : gestion de projet, animation de réseau, marketing, management, (à compléter)
	</p>
	<p>
	<strong>Présentation personnelle</strong> : l’Utilisateur est invité à se présenter en quelques mots, ce texte de présentation est volontairement limité à 560 caractères afin d’inciter l’Utilisateur à se concentrer sur l’essentiel. L’Utilisateur ne doit pas utiliser cet espace pour entrer dans le détail de ses projet, la partie "projet" est dédiée à cet effet.

	</p>
	<p>
	<strong>2.1.2. Confidentialité</strong>
	</p>
	<p>
	L’Utilisateur du réseau social RRR dispose de trois niveaux de confidentialité paramétrable dans l’onglet "confidentialité" de son profil :
	</p>
	<p>
	<strong>A. Privé</strong> : donne accès à la consultation et aux outils de communication de l’outil RRR mais limite la consultation de son profil à ses contacts directs. Précision sur la notion de contact direct : une fois son inscription terminé, l’Utilisateur "membre" a la possibilité d’emettre ou de recevoir des demandes de «contacts directs". Une fois qu’il accepte une demande ou qu’une demande émise et acceptée par l’autre "membre", les deux "membres" sont considérés comme contacts directs.
	</p>
	<p>
	<strong>B. Semi-privé </strong>: donne accès à la consultation et aux outils de communication de l’outil RRR, rend le profil de l’Utilisateur visible uniquement par les membres inscrits sur le réseau social RRR.
	</p>

	<p>
	<strong>C. Public</strong> : tous les internautes (inscrits sur le réseau social RRR ou non) peuvent consulter le profil de l’Utilisateur. Un profil public est susceptible d’être référencé sur les moteurs de recherche (Google, Yahoo!, etc.).
	</p>
	<p>
	<strong>2.1.3. Fonctionnalité de communication</strong>
	</p>
	<p>
	Chaque Utilisateur inscrit sur le réseau social RRR bénéficie des fonctionnalités de communication : mise en relation (ajout d’un membre dans sa liste de contact), messagerie privée (envoi d’un message privé directement à un Utilisateur), chat (dialogue en direct avec les contacts connectés), invitation à rejoindre un projet.
	</p>
	<p>
	<strong>Aucune de ces fonctionnalités de communication ne doit être utilisée pour une communication commerciale. Les administrateurs de la plateforme se réservent le droit de supprimer toute communication à caractère commerciale. Les administrateurs de la plateforme se réservent le droit de bloquer l'accès au service à l'Utilisateur qui ne respecterait pas ces conditions. Les administrateurs se réservent le droit de supprimer tout compte Utilisateur qui ne respecterait pas ces conditions.</strong>
	</p>
	<p>

	<strong>2.2. Espaces "projet"</strong>
	</p>
	<p>
	<strong>Introduction</strong>
	Le réseau social RRR propose aux Utilisateurs de créer puis d’animer un(e) ou plusieurs espace(s) ou fiche(s) "projet". L'Utilisateur, créateur d’une fiche "projet" est identifié par défaut comme "référent" sur la fiche du projet créé. A tout moment un autre membre peut demander d’être "référent" en s'adressant aux administrateurs de la plateforme.
	</p>
	<p>
	<strong>2.2.1. Référencement d’un projet</strong>
	</p>
	<p>
	Un "projet" désigne tout projet ou initiative menée sur le territoire picard et concernant les problématiques du développement rural. <strong>Le référencement d'une fiche "projet" ne doit en aucun cas être utilisé comme lieu de communication commerciale.</strong> <strong>Les administrateurs du réseau social RRR se réservent le droit de supprimer toute fiche projet qui ne respecterait pas ces conditions.</strong>

	</p>
	<p>
	L'Utilisateur peut référencer un projet. Une fois son inscription terminée en se rendant dans la rubrique "projets", puis en cliquant : "créer un projet". Le formulaire est construit de manière à guider l’Utilisateur vers une présentation synthétique et qualifiée de son projet. Certains champs sont volontairement limités en nombre de caractères, incitant le porteur de projet à synthétiser ses propos. La création d’un espace "projet" demande au référent de faire un exercice de synthèse et de respecter des champs formatés afin de donner de la lisibilité à son projet et de permettre à l’outil de suggérer son projet aux personnes qui peuvent êtres concernées. <strong>Les administrateurs du réseau social RRR se réservent le droit de modifier ou de compléter une fiche "projet" afin d'en faciliter la lisibilité et la compréhension de la part des autres Utilisateurs de la plateforme.</strong>
	</p>
	<p>
	<strong>2.2.2. Rejoindre un espace "projet"</strong>
	</p>
	<p>
	Tous les Utilisateurs inscrits sur le réseau social RRR peuvent rejoindre un ou plusieurs projet(s) en tant qu’observateur ou contributeur.
	</p>
	<p>
	<strong>2.2.2.1. Les référents</strong> "projet"

	</p>
	<p>
	Le créateur d’un projet est automatiquement identifié comme référent du projet sur le réseau social RRR, les membres qui souhaitent apparaître en tant que référent sur une fiche ou un espace projet doivent formuler leur demande aux administrateurs du réseau social RRR.
	</p>
	<p>
	<strong>Droits des référents projet</strong> : Le(s) référent(s) a/ont la possibilité de modifier les informations du projet (fiche) et d’approuver les demandes d’adhésion d’autres Utilisateurs en tant que contributeurs. Il(s) bénéficie(nt) également des mêmes droits de publication que les contributeurs (publication d’une ressource, d’une idée, de commentaires). Les référents sont identifiés comme tels sur la fiche du projet. Ce statut de référent n’a aucune valeur officielle. Le référent ne peut être tenu responsable des contenus publiés par les contributeurs, cependant dans le cadre de la charte, un référent de projet s’engage auprès des éditeurs du réseau social RRR à signaler toute publication inadaptée ou mauvaise utilisation de son espace collaboratif "projet".
	</p>
	<p>
	<strong>2.2.2.2. Contributeur ou Observateur</strong>
	</p>
	<p>
	Au moment de rejoindre un projet, l’Utilisateur peut choisir entre deux statuts : Contributeur ou Observateur. Le statut de Contributeur concerne les membres à part entière de l’équipe d’un projet, ou toute personne directement impliquée dans un projet. Le statut Contributeur donne notamment le droit à la publication de ressources dans l’espace ressources du Projet. L’obtention du statut de Contributeur est soumis à la validation d’un Utilisateur, référent du projet. Pour les Utilisateurs souhaitant simplement suivre les actualités d’un projet, le statut "observateur" est recommandé. Ce statut permet aussi à l’Utilisateur de manifester son intérêt pour un projet et de recevoir des informations sur son tableau de bord ainsi que par mail dans la synthèse hebdomadaire du réseau. Le statut d’Observateur est libre et ne nécessite pas de validation de la part d’un référent du projet.
	</p>
	<p>

	<strong>2.2.3. Fonctionnalités d’un espace projet</strong>
	</p>
	<p>
	Les fonctionnalités d’un espace projet sont activées pour les créateurs de l’espace ("référents") ou par les Utilisateurs du réseau social RRR ayant rejoint l’espace en tant que "contributeur".
	</p>
	<p>
	<strong>2.2.3.1 Publication d’une ressource</strong>
	</p>
	<p>
	L’Utilisateur a la possibilité de publier un document (formats acceptés : pdf, xls, doc) au sein de l’espace projet. Tous les membres pourront alors télécharger et consulter les documents publiés. L'Utilisateur qui publie un document dans l'espace "ressources" d'un "projet" atteste disposer de l'ensemble des droits sur le document diffusé ou de l'autorisation expresse de l'auteur du document pour sa diffusion. Quelle que soit la nature du document diffusé, l'Utilisateur doit renseigner la source dudit document grâce au champ prévu à cet effet lors de l'envoi du fichier sur la plateforme.
	</p>
	<p>
	<strong>2.2.3.2 Publication d’une idée</strong>
	</p>

	<p>
	L’Utilisateur a la possiblité de soumettre une idée (composée d’un titre et d’une description). Cette idée sera publiée dans l’espace "idées" de l’espace "projet" et son auteur identifié par une photo miniature. Les Utilisateurs "référents" et "contributeurs" de l’espace "projet" ont la possibilité de "voter pour" l’idée ou d’ajouter un "commentaire" à l’idée.
	</p>
	<p>
	<strong>2.3. Les "structures"</strong>
	</p>
	<p>
	Lors de son inscription sur le réseau RRR, l’Utilisateur a la possiblilité de lister la ou les organisations ("structures") dont il fait partie. On désigne par "structure" toute entreprise ou toute organisation (association, collectivité) pour laquelle l’Utilisateur travaille ou s’implique (en tant qu’adhérent par exemple). Lors de son inscription, l’Utilisateur peut ajouter ou supprimer une appartenance à une ou plusieurs "structure(s)". Après l'inscription, l'Utilisateur peut modifier ou ajouter des liens d'appartenance aux "structures" via l’espace "modifier mon profil", sous l'onglet "structures". L'Utilisateur ayant référencé le premier une "structure" dispose des droits pour compléter ou modifier la fiche "structure". Pour cela il doit se connecter sur le réseau social RRR, se rendre sur la fiche de la structure puis cliquer sur le bouton "éditer".
	</p>
	<p>
	L’Utilisateur est libre de référencer ou non la ou les structure(s) dont il fait partie. L'Utilisateur atteste faire légitimement partie de ladite/lesdites structure(s). L’Utilisateur atteste avoir l’accord de sa hiérarchie si cela est nécessaire, pour référencer sa structure sur le réseau social RRR. En cas d’usurpation d’identité ou d'information erronée concernant une structure référencée sur le réseau social RRR, le représentant légal de l'entreprise ou l'organisation concernée peut s’adresser directement à l’éditeur du réseau social RRR qui se réserve les droits de suppression ou de modification des informations.
	</p>
	<p>
	<strong>2.3.1. Statuts "référent" d'un Utilisateur vis-à-vis d’une "structure" :</strong>
	</p>

	<p>
	Le premier Utilisateur qui référence une entité sur le réseau social RRR, est, par défaut, identifié comme référent sur la fiche "structure" et dispose des droits d'édition et de modification de la fiche. Toute demande d'obtention du statut de référent par un autre Utilisateur doit être adressée à l'équipe d'animation du réseau social RRR.
	</p>
	<p>
	<strong>3. Publication de contenus, images, documents :</strong>
	</p>
	<p>
	L’Utilisateur du réseau social RRR a la possibilité de publier des contenus écrits, des documents (dans la partie ressources des Espaces projet), ainsi que des images pour illustrer les différentes fiches (profil, structure, projet). L’Utilisateur atteste disposer de l'ensemble des droits sur les contenus et documents publiés. Les éditeurs du réseau social RRR ne sauraient être tenus responsable de l'hébergement de documents publiés par un Utilisateur non titulaire des droits ou sans conssentement expresse du ou des auteurs. Toute requête en cas de contenu ou document publié sur la plateforme sans l’autorisation de son auteur doit être envoyée à contact@reseau-rural-picardie.eu et sera immédiatement traitée par l’équipe d’animation du réseau.
	</p>
	<p>
	<strong>4. Responsabilité</strong>
	</p>
	<p>
	Les éditeurs du réseau social RRR ne sauraient être tenu responsables des conséquences d’une mauvaise interprétation des informations publiées ou échangées par les Utilisateurs via l'outil réseau social RRR.
	</p>

	<p>
	<strong>5. Rôles et statuts des Utilisateurs vis-à-vis des fiches "projet" et "structure" : </strong>
	</p>
	<p>
	L’objectif du réseau social RRR est d’identifier et de distinguer de manière informelle les référents et les contributeurs des structures et projets référencés. Les informations concernant l’appartenance d'un Utilisateur (personne physique) à une "structure" (personne morale), ou la référence d’un Utilisateur à un "projet", ou la référence d’un "projet" à une "structure" n’ont aucun caractère officiel. Tout représentant légal de l'entreprise ou de l'organisation concernée, souhaitant corriger ou supprimer une information concernant son entreprise ou son organisation peut prendre contact avec l’équipe d’animation du réseau social RRR. L’éditeur du réseau RRR ne saurait être tenu responsable des conséquences d’une mauvaise interprétation des informations publiées par les Utilisateurs.
	</p>
	<p>
	<strong>6. Suppression du compte Utilisateur</strong>
	</p>
	<p>
	L'Utilisateur peut supprimer son compte à tout moment. La suppression de son compte entrainera la suppression de l'ensemble des données personnelles liées au compte de l'Utilisateur. Les contenus générés par l'Utilisateur (référencement d'une structure, création d'un projet) sont susceptibles de rester visibles sur le réseau social RRR. La référence à son auteur sera en revanche invisible.
	</p>
